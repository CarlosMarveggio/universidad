    private void btnMateriaBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMateriaBuscarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(txtMateriaId.getText());
        Materia materia = materiaData.buscarMateria(id);
        if(materia!=null){
            txtMateriaId.setText(materia.getIdMateria()+"");
            txtMateriaAnio.setText(materia.getAnio()+"");
            txtMateriaNombre.setText(materia.getNombre());
            if (materia.isEstado()==true){
                lblMateriaEstado.setText("Activa");
            }else if (materia.isEstado()==false){
                lblMateriaEstado.setText("Inactiva");
            }
        }
        
    }//GEN-LAST:event_btnMateriaBuscarActionPerformed

    private void btnMateriaActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMateriaActualizarActionPerformed
        // TODO add your handling code here:
        if(txtMateriaId.getText()!=null){
            int id = Integer.parseInt(txtMateriaId.getText());
            int anio = Integer.parseInt(txtMateriaAnio.getText());
            String nombre = txtMateriaNombre.getText();
            String activo = lblMateriaEstado.getText();
            boolean estado;            
            if ("Activo".equals(activo)){
                estado = true;
            }else{
                estado = false;
            }
            Materia materia = new Materia(id, nombre, anio, estado);
            materiaData.modificarMateria(materia);
        }        
    }//GEN-LAST:event_btnMateriaActualizarActionPerformed


